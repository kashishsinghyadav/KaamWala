package com.kaamwala.entity;

/**
 * Enumeration of all service categories offered on the KaamWala platform.
 *
 * <p>Each value represents a specific trade or skill that workers can register under
 * and customers can search for when posting jobs.</p>
 */
public enum ServiceCategory {

    /** Woodworking, furniture repair, door/window fitting */
    CARPENTER,

    /** Electrical wiring, switch/socket installation, fault repair */
    ELECTRICIAN,

    /** Pipe fitting, leak repair, bathroom/kitchen plumbing */
    PLUMBER,

    /** Interior/exterior wall painting, polishing, texture work */
    PAINTER,

    /** AC installation, servicing, gas refilling, repair */
    AC_TECHNICIAN,

    /** CCTV camera installation, wiring, NVR/DVR setup */
    CCTV_INSTALLER,

    /** RO water purifier installation, filter change, servicing */
    RO_SERVICE,

    /** Deep cleaning, regular cleaning, pest control */
    HOME_CLEANING,

    /** Brickwork, plastering, tiling, construction labour */
    MASON,

    /** Metal welding, grille fabrication, gate repair */
    WELDER,

    /** Custom furniture design, modular kitchen, wardrobe making */
    FURNITURE_MAKER
}
